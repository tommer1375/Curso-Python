# Tuplas: Son colecciones de datos ordenados e inmutables

# Propiedades: 
# Inmutabilidad: Una vez creada no podemos modificar sus datos
# Indexacion: Se puede acceder a los datos mediante indices (valor numerico)
# Ordenadas: Mantienen el orden en el que se insertan los elementos

# Metodos: 
# count(): Cuenta cuantas veces aparece un valor
# index(): Devuelve en indice del primer elemento

# Ejemplos:
tupla = (1,2,3,4,5,2,2)
# acceso o lectura
tupla[0] = 0 # error

print(tupla.count(2)) # 3
print(tupla.index(3)) # 2





